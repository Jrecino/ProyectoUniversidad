    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
      ClsMascotas objetoMascotas = new ClsMascotas();
    int fila = tbTotalMascotas.getSelectedRow();
    if (fila >= 0) {
        int id = Integer.parseInt(txtId.getText());
        String tipoanimal = txttipoanimal.getText();
        int edad = Integer.parseInt(txtedad.getText());
        String nombre = txtnombre.getText();
        String diagnostico = txtdiagnostico.getText();
        String procedimiento = txtprocedimiento.getText();
        
        objetoMascotas.ModificarMascota(id, tipoanimal, edad, nombre, diagnostico, procedimiento);
        objetoMascotas.MostrarMascotas(tbTotalMascotas);
    } else {
        JOptionPane.showMessageDialog(null, "Fila no seleccionada");
    }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        ClsMascotas objetoMascotas = new ClsMascotas();
    int fila = tbTotalMascotas.getSelectedRow();
    if (fila >= 0) {
        int Id = Integer.parseInt(txtId.getText());
        objetoMascotas.ElminarMascotas(txtId);
        objetoMascotas.MostrarMascotas(tbTotalMascotas);
    } else {
        JOptionPane.showMessageDialog(null, "Fila no seleccionada");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

